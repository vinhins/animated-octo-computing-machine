#!/bin/bash

# Guide for analyzing HTTP API calls to MetaQuote servers
# This script helps capture and analyze network traffic

PHONE_HOME="/data/data/com.termux/files/home"

echo "=== HTTP API Analysis Setup ==="
echo ""
echo "Method 1: Use tcpdump to capture network traffic"
echo "Prerequisites: Install tcpdump in Termux"
echo ""
echo "# On your phone via ADB:"
adb shell "echo 'apt update && apt install -y tcpdump' | xargs echo"
echo ""
echo "# Start packet capture:"
adb shell "echo 'tcpdump -i any -w /data/data/com.termux/files/home/capture.pcap port 80 or port 443' | xargs echo"
echo ""
echo "# In another terminal, run API call:"
adb shell "export LD_LIBRARY_PATH=$PHONE_HOME && cd $PHONE_HOME && java -cp . net.metaquotes.tools.BrokerSignature --api http://BROKER_API_URL action=search"
echo ""
echo "# Transfer capture file:"
adb pull /data/data/com.termux/files/home/capture.pcap ./capture.pcap
echo ""
echo "# Analyze with Wireshark or use strings:"
echo "strings ./capture.pcap | grep -E '(GET|POST|Authorization|Signature|Bearer)'"
echo ""
echo "------"
echo ""
echo "Method 2: Use Frida to intercept Java HTTP calls (requires Frida server)"
echo "This approach hooks Java's HttpURLConnection class"
echo ""
echo "------"
echo ""
echo "Method 3: Proxy approach - Route traffic through mitmproxy"
echo "Prerequisites: mitmproxy on your laptop"
echo ""
echo "# Start mitmproxy on your laptop:"
echo "mitmproxy -p 8080 --mode transparent"
echo ""
echo "# On phone, route traffic through your laptop:"
echo "route add default gw 192.168.X.X"
echo ""
echo "# Then run your app - traffic will be visible in mitmproxy"
echo ""
echo "------"
echo ""
echo "Method 4: Simple logging (already built into BrokerSignature.java)"
echo ""
echo "# Run and capture logs:"
adb shell "export LD_LIBRARY_PATH=$PHONE_HOME && cd $PHONE_HOME && java -cp . net.metaquotes.tools.BrokerSignature --api http://example.com action=search 2>&1 | tee api_output.log"
echo ""
echo "# Pull logs:"
adb pull /data/data/com.termux/files/home/api_output.log ./api_output.log
echo ""
echo "=== Key things to analyze ==="
echo "1. Request signature format"
echo "2. How signature is used (header, parameter, custom format)"
echo "3. Request/response encryption"
echo "4. Authentication headers/tokens"
echo "5. Query parameter format"
echo "6. Response parsing"
